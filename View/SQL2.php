    <h1>Chapitre 2 : Bases de données et tables</h1>

    <h2>Création d'une base de données</h2>
    <p>Une base de données est un ensemble structuré de données organisé pour être facilement accessible, géré et mis à jour. 
    En SQL, une base de données peut être créée avec la commande <code>CREATE DATABASE</code>.</p>

    <h3>Exemple :</h3>
    <pre><code>CREATE DATABASE MaBaseDeDonnees;</code></pre>

    <h2>Création et gestion des tables</h2>
    <p>Les tables sont les structures principales des bases de données relationnelles. Elles contiennent des colonnes et des lignes, où chaque colonne représente un champ et chaque ligne représente un enregistrement.</p>

    <h3>Exemple de création d'une table :</h3>
    <pre><code>
CREATE TABLE Utilisateurs (
    ID INT PRIMARY KEY,
    Nom VARCHAR(50),
    Email VARCHAR(100),
    DateInscription DATE
);
    </code></pre>

    <h2>Types de données</h2>
    <p>Chaque colonne d'une table doit avoir un type de données spécifique qui définit le type de valeur qu'elle peut contenir :</p>
    <ul>
        <li><code>INT</code> : pour les nombres entiers</li>
        <li><code>VARCHAR</code> : pour les chaînes de caractères</li>
        <li><code>DATE</code> : pour les dates</li>
        <li><code>BOOLEAN</code> : pour les valeurs booléennes</li>
    </ul>
